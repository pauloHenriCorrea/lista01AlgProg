"""
Exercício 2: Escreva um programa que recebe um número digitado pelo usuário e mostra a
seguinte mensagem: O número informado foi [número].
"""

# Emtrada de dados
numero = int(input("Informe um número qualquer: "))

# Computação
concatenacao = "O número digitado foi: " + str(numero)

# Saída de dados
print(concatenacao)

