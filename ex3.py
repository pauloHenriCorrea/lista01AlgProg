"""
Exercício 3: Escreva um programa que recebe dois números digitados pelo usuário e imprime
a soma desses dois números.
"""

# Entrada de dados
numero1 = int(input("Informe o primeiro número: "))
numero2 = int(input("Informe o segundo número número: "))

# Computação
soma = numero1 + numero2

concatenacao = "A soma de " + str(numero1) + " + " + str(numero2) + " = " + str(soma)

# Saída de dados
print(concatenacao)

# Exercício finalizado